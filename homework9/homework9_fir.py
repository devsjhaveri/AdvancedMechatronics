filteredA = []
for i in range(len(dataA)):
    val = 0
    for j in range(len(weights_A)):
        if i - j >= 0:
            val += weights_A[j] * dataA[i - j]
    filteredA.append(val)

Fs_A = len(t_A) / (t_A[-1] - t_A[0])
n_A = len(dataA)
k_A = np.arange(n_A)
frq_A = k_A / (n_A / Fs_A)
frq_A = frq_A[range(int(n_A/2))]
Y_A = np.fft.fft(dataA) / n_A
Y_A = Y_A[range(int(n_A/2))]
Y_A_filtered = np.fft.fft(filteredA) / n_A
Y_A_filtered = Y_A_filtered[range(int(n_A/2))]

fig, (ax1, ax2) = plt.subplots(2, 1)
ax1.plot(t_A, dataA, 'k')
ax1.plot(t_A, filteredA, 'r')
ax1.set_xlabel('Time')
ax1.set_ylabel('Amplitude')
ax1.set_title(f'sigA FIR cutoff=50Hz bw=800 Blackman')
ax2.loglog(frq_A, abs(Y_A), 'k')
ax2.loglog(frq_A, abs(Y_A_filtered), 'r')
ax2.set_xlabel('Freq (Hz)')
ax2.set_ylabel('|Y(freq)|')
ax2.set_title('sigA - FFT')
plt.tight_layout()

